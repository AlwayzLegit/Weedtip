/** Bbox centered on a point, spanning ~`radiusMeters` in each direction. */
export function bboxAround(lat: number, lng: number, radiusMeters: number): BBox {
  const dLat = radiusMeters / 111_320;
  const dLng = radiusMeters / (111_320 * Math.max(Math.cos((lat * Math.PI) / 180), 0.2));
  return [lng - dLng, lat - dLat, lng + dLng, lat + dLat];
}
